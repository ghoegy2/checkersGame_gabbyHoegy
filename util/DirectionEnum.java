package cpsc2150.extendedCheckers.util;

/**
 * DirectionEnum is an enumeration designed to represent the four directions a piece is allowed to move in.
 * In DirectionEnum.java, each possible direction is represented with an abbreviated cardinal direction (NE, NW, SE,
 * SW).
 *
 * @invariant [NE means northeast] AND [NW means northwest] AND [SE means southeast] AND [SW means southwest]
 */
public enum DirectionEnum {
    NE,
    NW,
    SE,
    SW
}
